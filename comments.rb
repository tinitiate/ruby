# >>CODE-TITLE - Single line Comments
# >>CODE-NOTES<<
# A single line comment begins with # character
# >><<
# >>CODE-RUBY-ALL<<
# This is a single line comment.
print "Hello World"
# >><<
# >>OUTPUT<<
# Hello World
# >><<


# >>CODE-TITLE - Multi line Comments
# >>CODE-NOTES<<
# A multi line comment begins with the string "=begin" and ends with "=end" 
# >><<
# >>CODE-RUBY-ALL<<
=begin
Welcome to World Of Coading 
Welcome to tinitate.com  
=end
print "Hello World"
# >><<
# >>OUTPUT<<
# Hello World
